package com.example.controller;

import com.baomidou.mybatisplus.core.conditions.query.QueryWrapper;
import com.baomidou.mybatisplus.extension.plugins.pagination.Page;
import com.example.annotation.AuthCheck;
import com.example.common.BaseResponse;
import com.example.common.ErrorCode;
import com.example.common.IdRequest;
import com.example.common.ResultUtils;
import com.example.common.model.domain.InterfaceInfo;
import com.example.common.model.domain.User;
import com.example.common.model.enums.InterfaceInfoStatusEnum;
import com.example.constant.CommonConstant;
import com.example.exception.BusinessException;
import com.example.model.dto.interfaceinfo.InterfaceInfoAddRequest;
import com.example.model.dto.interfaceinfo.InterfaceInfoInvokeRequest;
import com.example.model.dto.interfaceinfo.InterfaceInfoQueryRequest;
import com.example.model.dto.interfaceinfo.InterfaceInfoUpdateRequest;

import com.example.service.InterfaceInfoService;
import com.example.service.UserService;

import com.google.gson.Gson;
import lombok.extern.slf4j.Slf4j;
import org.apache.commons.lang3.StringUtils;
import org.springframework.beans.BeanUtils;
